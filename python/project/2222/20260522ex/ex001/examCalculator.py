def addition(n1, n2):
    print(f'덧셈: {n1 + n2}')

def subtraction(n1, n2):
    print(f'뺄셈: {n1 - n2}')

def multiplication(n1, n2):
    print(f'곱셈: {n1 * n2}')

def division(n1, n2):
    print(f'나눗셈: {n1 / n2}')

def rest(n1, n2):
    print(f'나머지: {n1 % n2}')

def portion(n1, n2):
    print(f'몫: {n1 // n2}')