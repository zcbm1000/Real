import turtle

t = turtle.Turtle()        # 그림 그리기 준비 완료
t.shape('turtle')          # 아이콘 설정 (거북이 모양)

angle = 120

t.right(angle)             # 우측으로 120만큼 회전
t.forward(300)             # 직진으로 300만큼 이동

t.left(angle)              # 좌측으로 120만큼 회전
t.forward(300)             # 직진으로 300만큼 이동

t.left(angle)              # 좌측으로 120만큼 회전
t.forward(300)             # 직진으로 300만큼 이동


